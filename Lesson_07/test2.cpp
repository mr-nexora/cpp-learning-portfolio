#include <iostream>
using namespace std;

int main()
{

    // Numeric Data Types

    // Int Data Types
    int myAge = 22;
    cout << "Int Data Type = " << myAge << endl;

    // Float Data Types
    float myNum = 2.99;
    cout << "Float Data Type = " << myNum << endl;

    // Double Data Types
    double myNUM = 3.99;
    cout << "Double Data Type = " << myNUM << endl;

    // Scientific Numbers
    float x = 35e3;
    double y = 12E4;

    cout << x << endl;
    cout << y << endl;

    return 0;
}