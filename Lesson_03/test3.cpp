#include <iostream>
using namespace std;

int main()
{

    // C++ New Lines

    // Eg 01:
    cout << "Hello, World! \n";
    cout << "I am learning C++";

    // Eg 02:
    cout << "Hello, World! " << "\n";
    cout << "I am learning C++";

    // Eg 03:
    cout << "Hello, World! \n\n";
    cout << "I am learning C++";

    // Eg 04:
    cout << "Hello, World! " << endl;
    cout << "I am learning C++";

    return 0;
}
