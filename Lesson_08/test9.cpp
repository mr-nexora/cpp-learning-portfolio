#include <iostream>
using namespace std;

int main()
{
    int x = 5;
    int y = 10;

    // Equal to x == y
    cout << (x == y) << endl; // Output: 0 (False)
    cout << "\n\n";

    // Not equal x != y
    cout << (x != y) << endl; // Output: 1 (True)
    cout << "\n\n";

    // Greater than x > y
    cout << (x > y) << endl; // Output: 0 (False)
    cout << "\n\n";

    // Less than x < y
    cout << (x < y) << endl; // Output: 1 (True)
    cout << "\n\n";

    // Greater than or equal to x >= y
    cout << (x >= y) << endl; // Output: 0 (False)
    cout << "\n\n";

    // Less than or equal to x <= y
    cout << (x <= y) << endl; // Output: 1 (True)

    return 0;
}
