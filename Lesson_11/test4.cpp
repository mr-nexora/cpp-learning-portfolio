#include <iostream>
using namespace std;

int main()
{

    // Boolean Expressions
    
    // Eg 01:
    int x = 10, y = 5;
    cout << (x > y) << "\n\n"; // Output is 1 (True) 
    cout << (y > x) << "\n\n"; // Output is 0 (False) 

    // Eg 02:
    int z  = 10;
    cout << (z == 10);


    return 0;
}
